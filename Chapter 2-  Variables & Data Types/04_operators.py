#Comparison Operators
# ==, !=, >, <, >=, <=
#Return value is either true or false

print(a==b) # False
print(a!=b) # True
print(a>b)  # False
print(a<b)  # True
print(a>=b) # False

#Logical Operators
# and, or, not
#Return value is either true or false
# In AND, Both Conditions should be true therefore it will return true
# IN OR, Either of the condition should be true, if any of the conditions is true, it wiill give true.
# In NOT, if the condition is true, it will give false

####TRUTH TABLE OF OR

print('Truth Table of OR is: \n')
print(True or True) # True
print(True or False) # True
print(False or False) # False
print(False or True) # True

####TRUTH TABLE OF AND
print('Truth Table of AND is: \n')
print(True and True) # True
print(True and False) # False
print(False and True) # False
print(False and False) # False

##NOT OPERATORS JUST CONVERTS THE TRUE INTO FALSE OR FALSE INTO TRUE. 

print('Truth Table of NOT is: \n')
print(not True) # False
print(not False) # True


